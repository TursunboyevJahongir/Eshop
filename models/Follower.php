<?php

namespace app\models;

use Yii;

/**
 * This is the model class for table "follower".
 *
 * @property int $id
 * @property int $shop_id
 * @property int $user_id
 *
 * @property Shop $shop
 * @property User $user
 */
class Follower extends \yii\db\ActiveRecord
{
    /**
     * {@inheritdoc}
     */
    public static function tableName()
